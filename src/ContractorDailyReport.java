   void total(){
       int skilledPers=Integer.parseInt(conSkillspin.getText());
     int skilledrate= Integer.parseInt(SkillrateCon.getText());
      int unskilledPers=Integer.parseInt(ConUnskillSpin.getText());
     int unskilledrate= Integer.parseInt(unskillratecon.getText());
     float advance =Float.parseFloat(conAdvancefield.getText());
     totalSkilled=skilledPers*skilledrate;
    
      totalunSkilled=unskilledPers*unskilledrate;
     
     grandTotal= totalSkilled+totalunSkilled;
    median= advance-grandTotal;
     skillTotalcon.setText(""+totalSkilled);
     unSkillTotalco.setText(""+totalunSkilled);
    
         
    }
    
